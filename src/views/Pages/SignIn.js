async function loginUser(credentials) {

  return fetch('https://exammanger.co.in/loginuser/?username='+ credentials.username +'&password='+ credentials.password , {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json'
    },
    // body: JSON.stringify(credentials)
  })
    .then(data => data.json())
 }
